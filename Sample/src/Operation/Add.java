package Operation;

import java.util.Scanner;

public class Add {
	
	public int add(int val1,int b)
	{
		
	int add = val1+b;
	
		return add;
	
	
	}
	public int add(int val1,int val2,int val3)
	{
		return val1+val2+val3;
		
	}
	public double add(int val1,double val2,int val3)
	{
		return val1+val2+val3;
		
	}
}
